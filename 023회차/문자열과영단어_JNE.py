def solution(s):
    number = {
        "zero": "0",
        "one": "1",
        "two": "2",
        "three": "3",
        "four": "4",
        "five": "5",
        "six": "6",
        "seven": "7",
        "eight": "8",
        "nine": "9"}
    
    # .items()는 파이썬의 딕셔너리 메서드
    # 딕셔너리에 있는 모든 key-value 쌍을 튜플 형태로 반환
    for a, b in number.items():
        s = s.replace(a, b)
    
    return int(s)
